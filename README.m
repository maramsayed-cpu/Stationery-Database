To run the server file, follow these steps:
1. open a terminal window in the project's directory
2. enter "npm init"
3. enter "npm install pug"
4. enter "npm install express"
5. enter "npm install body-parser"
6. enter "npm install sqlite3"
7. enter "node server.js"
